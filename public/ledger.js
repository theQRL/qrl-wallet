function serialize(CLA, INS, p1 = 0, p2 = 0, data = null) {
    var size = 5;

    var buffer = Buffer.alloc(size);
    buffer[0] = CLA;
    buffer[1] = INS;
    buffer[2] = p1;
    buffer[3] = p2;
    buffer[4] = 0;

    if (data != null) {
        if (data.length > 255) {
            throw new Error('maximum data size = 255');
        }
        buffer[4] = data.length;

        // Add data to end of buffer
        buffer = concatenateTypedArrays(
          Uint8Array,
            buffer,
            data
        )
    }

    return buffer;
}

